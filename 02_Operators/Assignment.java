// Assignment Operator

import java.util.*;

public class Assignment {
    public static void main(String args[]) {

        // = -> For assigning value 
        
        int a = 5;
        int b = a;
        System.out.println(a);
        System.out.println(b);


         // += 

         int A = 10;
         // A = A + 10;
         A += 10;
         System.out.println(A);
         
         // -= 

         int B = 10;
         // B = B - 5;
         B -= 5;
         System.out.println(B);

         
         // *= 

         int C = 10;
         // C = C * 10;
         C *= 10;
         System.out.println(C);

         
         // /= 

         int D = 10;
         // D = D / 10;
         D /= 10;
         System.out.println(D);
         
         // %= 

         int E = 10;
         // E = E % 10;
         E %= 10;
         System.out.println(E);
    }
}